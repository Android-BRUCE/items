package cn.zrcx.action.form;

import java.util.Date;

public class CustomerForm {
	private int c_id;
	private String c_companyname;
	private String c_linkman;
	private Date c_startdate;
	private String c_address;
	private String c_phone;
	private String c_mobilephone;
	private String c_intro;
	private String c_comment;
	private Date c_update;
	
	
	
	
	public CustomerForm(){}
	public CustomerForm(String c_companyname, String c_linkman,
			String c_address, String c_phone, String c_mobilephone,
			String c_intro, String c_comment) {
		
		this.c_companyname = c_companyname;
		this.c_linkman = c_linkman;
		this.c_address = c_address;
		this.c_phone = c_phone;
		this.c_mobilephone = c_mobilephone;
		this.c_intro = c_intro;
		this.c_comment = c_comment;
		
	}
	
	public CustomerForm(String c_companyname, String c_linkman, Date c_startdate,
			String c_address, String c_phone, String c_mobilephone,
			String c_intro, String c_comment, Date c_update) {
		
		this.c_companyname = c_companyname;
		this.c_linkman = c_linkman;
		this.c_startdate = c_startdate;
		this.c_address = c_address;
		this.c_phone = c_phone;
		this.c_mobilephone = c_mobilephone;
		this.c_intro = c_intro;
		this.c_comment = c_comment;
		this.c_update = c_update;
	}
	public CustomerForm(int c_id, String c_companyname, String c_linkman,
			Date c_startdate, String c_address, String c_phone,
			String c_mobilephone, String c_intro, String c_comment,
			Date c_update) {
		
		this.c_id = c_id;
		this.c_companyname = c_companyname;
		this.c_linkman = c_linkman;
		this.c_startdate = c_startdate;
		this.c_address = c_address;
		this.c_phone = c_phone;
		this.c_mobilephone = c_mobilephone;
		this.c_intro = c_intro;
		this.c_comment = c_comment;
		this.c_update = c_update;
	}
	public int getC_id() {
		return c_id;
	}
	public void setC_id(int c_id) {
		this.c_id = c_id;
	}
	public String getC_companyname() {
		return c_companyname;
	}
	public void setC_companyname(String c_companyname) {
		this.c_companyname = c_companyname;
	}
	public String getC_linkman() {
		return c_linkman;
	}
	public void setC_linkman(String c_linkman) {
		this.c_linkman = c_linkman;
	}
	public Date getC_startdate() {
		return c_startdate;
	}
	public void setC_startdate(Date c_startdate) {
		this.c_startdate = c_startdate;
	}
	public String getC_address() {
		return c_address;
	}
	public void setC_address(String c_address) {
		this.c_address = c_address;
	}
	public String getC_phone() {
		return c_phone;
	}
	public void setC_phone(String c_phone) {
		this.c_phone = c_phone;
	}
	public String getC_mobilephone() {
		return c_mobilephone;
	}
	public void setC_mobilephone(String c_mobilephone) {
		this.c_mobilephone = c_mobilephone;
	}
	public String getC_intro() {
		return c_intro;
	}
	public void setC_intro(String c_intro) {
		this.c_intro = c_intro;
	}
	public String getC_comment() {
		return c_comment;
	}
	public void setC_comment(String c_comment) {
		this.c_comment = c_comment;
	}
	public Date getC_update() {
		return c_update;
	}
	public void setC_update(Date c_update) {
		this.c_update = c_update;
	}

}
